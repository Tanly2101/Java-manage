    void hienThiDS() {
        if (manvTXT == null) {
            DSNV = QuanLy.layTatCaNhanVien();
        } else {
            DSNV = QuanLy.TimNhanVien(manvTXT);
        }

        tableModel.setRowCount(0);

        DSNV.forEach((nhanvien) -> {
            tableModel.addRow(new Object[]{nhanvien.getManv(), nhanvien.getTennv(),
                nhanvien.getChucvu(), nhanvien.getGioitinh(), nhanvien.getNgaysinh(), nhanvien.getQuequan()});
        });
    }

