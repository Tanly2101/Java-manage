    private void BangNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BangNhanVienMouseClicked
        
        int selectedIndex = BangNhanVien.getSelectedRow();
        if (selectedIndex >= 0) {
        NhanVien nv = DSNV.get(selectedIndex);
        maNV = nv.getManv();
        tennv = nv.getTennv();
        chucvu = nv.getChucvu();
        gioitinh = nv.getGioitinh();
        ngayinh = nv.getNgaysinh();
        quequan = nv.getQuequan();
        
        

        }
    }//GEN-LAST:event_BangNhanVienMouseClicked

